package uan.edu.co.NominaContable;

public class DescuentosYDeducciones {
	
	private int salud;
	private int pension;
	private int prestamos;
	private int embargos;
	private int fondoEmpleados;
	public int getSalud() {
		return salud;
	}
	public void setSalud(int salud) {
		this.salud = salud;
	}
	public int getPension() {
		return pension;
	}
	public void setPension(int pension) {
		this.pension = pension;
	}
	public int getPrestamos() {
		return prestamos;
	}
	public void setPrestamos(int prestamos) {
		this.prestamos = prestamos;
	}
	public int getEmbargos() {
		return embargos;
	}
	public void setEmbargos(int embargos) {
		this.embargos = embargos;
	}
	public int getFondoEmpleados() {
		return fondoEmpleados;
	}
	public void setFondoEmpleados(int fondoEmpleados) {
		this.fondoEmpleados = fondoEmpleados;
	}

	
	
}
